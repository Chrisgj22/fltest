let currentQuestionIndex = 0;
let score = 0;
let wrongAnswers = 0;

function loadQuestion() {
    const questionContainer = document.getElementById('question-container');
    questionContainer.innerHTML = '';

    const currentQuestion = questions[currentQuestionIndex];

    const questionElement = document.createElement('p');
    questionElement.textContent = currentQuestion.question;
    questionContainer.appendChild(questionElement);

    for (const key in currentQuestion.answers) {
        const button = document.createElement('button');
        button.textContent = `${key}: ${currentQuestion.answers[key]}`;
        button.onclick = () => checkAnswer(key, button);
        questionContainer.appendChild(button);
    }
}

function checkAnswer(answer, selectedButton) {
    const currentQuestion = questions[currentQuestionIndex];
    const buttons = document.querySelectorAll('#question-container button');

    // Highlight the correct answer in green
    buttons.forEach(button => {
        if (button.textContent.startsWith(currentQuestion.correctAnswer)) {
            button.style.backgroundColor = 'green';
        } else {
            button.style.backgroundColor = 'red';
        }
    });

    // Disable all buttons after an answer is clicked
    buttons.forEach(button => {
        button.disabled = true;
    });

    if (answer === currentQuestion.correctAnswer) {
        score++;
    } else {
        wrongAnswers++;
    }

    document.getElementById('score').textContent = `Score: ${score}, Wrong Answers: ${wrongAnswers}`;
    document.getElementById('next-button').style.display = 'block';
}

function loadNextQuestion() {
    currentQuestionIndex++;
    if (currentQuestionIndex < questions.length) {
        loadQuestion();
        document.getElementById('next-button').style.display = 'none';
    } else {
        document.getElementById('question-container').innerHTML = '<p>Quiz Completed!</p>';
        document.getElementById('next-button').style.display = 'none';
    }
}

window.onload = () => {
    loadQuestion();
    document.getElementById('next-button').style.display = 'none';
};
